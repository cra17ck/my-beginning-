        X_cols = [col for col in adl_data.columns if col != 'Y']
        X = add_constant(adl_data[X_cols])
        y = adl_data['Y']

        # Оценка ADL модели
        adl_model = OLS(y, X).fit()

        print("Результаты ADL модели (зависимая переменная: разность цен Транснефти):")
