# Расчет метрик для всех моделей
results = []

for model_name in all_forecasts.columns:
    if model_name not in ['Date', 'Close']:
        actual = all_forecasts['Close']
        forecast = all_forecasts[model_name]

        # Проверяем, что прогнозы не NaN
        if forecast.isna().any():
            continue

        mae = mean_absolute_error(actual, forecast)
        rmse = np.sqrt(mean_squared_error(actual, forecast))
        mape = mean_absolute_percentage_error(actual, forecast) * 100

        # Рассчитываем R²
        ss_res = np.sum((actual - forecast) ** 2)
        ss_tot = np.sum((actual - np.mean(actual)) ** 2)
        r2 = 1 - (ss_res / ss_tot) if ss_tot != 0 else 0

        # Directional Accuracy (точность направления)
        direction_actual = np.sign(actual.diff().dropna())
        direction_forecast = np.sign(forecast.diff().dropna())

        # Проверяем, что массивы имеют одинаковую длину
        min_len = min(len(direction_actual), len(direction_forecast))
        if min_len > 0:
            direction_actual = direction_actual.iloc[:min_len]
            direction_forecast = direction_forecast.iloc[:min_len]
            directional_accuracy = np.mean(direction_actual == direction_forecast) * 100
        else:
            directional_accuracy = 0

        results.append({
            'Модель': model_name,
            'MAE': mae,
            'RMSE': rmse,
            'MAPE (%)': mape,
            'R²': r2,
            'Точность направления (%)': directional_accuracy
        })

