def check_stationarity(series, name="Ряд"):
    result = adfuller(series.dropna())
    print(f"Тест Дики-Фуллера для {name}:")
    print(f"ADF Statistic: {result[0]:.4f}")
    print(f"p-value: {result[1]:.4f}")
    if result[1] <= 0.05:
        print(f"Вывод: {name} стационарен (p-value < 0.05)")
    else:
        print(f"Вывод: {name} нестационарен (p-value > 0.05)")
    print("-" * 50)
    return result[1]

